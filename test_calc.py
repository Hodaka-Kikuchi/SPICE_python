import numpy as np

# Bの定義（4x6行列）
B = np.array([
    [0.708023764,  0.706188607, 0,  0.708023764, -0.706188607, 0],
    [-0.706188607, 0.708023764, 0,  0.706188607,  0.708023764, 0],
    [0,            0,           1,  0,           0,           -1],
    [5.489172023,  0,           0, -5.489172023, 0,            0]
])

# HFの定義（6x6行列）
HF = np.array([
    [2.14e-5,  -2.14e-5, 0,         0,         0,         0],
    [-2.14e-5, 3.08e-5,  0,         0,         0,         0],
    [0,        0,        2.0076e-4, 0,         0,         0],
    [0,        0,        0,         2.14e-5,   2.14e-5,   0],
    [0,        0,        0,         2.14e-5,   3.08e-5,   0],
    [0,        0,        0,         0,         0,         2.0076e-4]
])

# B * HF * B.T の計算
result = B @ HF @ B.T

# 結果表示（小数点以下を整えて見やすく）
np.set_printoptions(precision=10, suppress=True)
print("Result of B @ HF @ B.T:")
print(result)
